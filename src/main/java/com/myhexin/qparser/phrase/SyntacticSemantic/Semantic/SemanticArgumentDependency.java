package com.myhexin.qparser.phrase.SyntacticSemantic.Semantic;

public class SemanticArgumentDependency {

    public SemanticArgumentDependency() {
    }

    public void setIndependentArguments_(SemanticArguments independentArguments_) {
		this.independentArguments_ = independentArguments_;
	}

	public void setDependentArguments_(SemanticArguments dependentArguments_) {
		this.dependentArguments_ = dependentArguments_;
	}

	public SemanticArguments getIndependentArguments()
    {
        return independentArguments_;
    }

    public SemanticArguments getDependentArguments()
    {
        return dependentArguments_;
    }

    private SemanticArguments independentArguments_ = new SemanticArguments();
    private SemanticArguments dependentArguments_ = new SemanticArguments();
 
}
