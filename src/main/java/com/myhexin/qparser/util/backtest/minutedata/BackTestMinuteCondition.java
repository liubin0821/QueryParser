package com.myhexin.qparser.util.backtest.minutedata;

public class BackTestMinuteCondition {

    private int startTime;

    private int endTime;

    private int stateLength;

    private String expression;

    private int timeAfterExpr;

    public String getExpression() {
        return expression;
    }

    public void setExpression(String expression) {
        this.expression = expression;
    }

    public int getTimeAfterExpr() {
        return timeAfterExpr;
    }

    public void setTimeAfterExpr(int timeAfterExpr) {
        this.timeAfterExpr = timeAfterExpr;
    }

    public int getStateLength() {
        return stateLength;
    }

    public void setStateLength(int stateLength) {
        this.stateLength = stateLength;
    }

    public int getStartTime() {
        return startTime;
    }

    public void setStartTime(int startTime) {
        this.startTime = startTime;
    }

    public int getEndTime() {
        return endTime;
    }

    public void setEndTime(int endTime) {
        this.endTime = endTime;
    }
    
    public String toString() {
        StringBuilder sb = new StringBuilder();
        sb.append("startTime:" + startTime).append(",");
        sb.append("endTime:" + endTime).append(",");
        sb.append("stateLength:" + stateLength).append(",");
        sb.append("expression:" + expression).append(",");
        sb.append("timeAfterExpr:" + timeAfterExpr);
        return sb.toString();
    }
}
